package basics;

public class Days {
	public static void main (String[] args) {
	//ejecutar diferentes bloques de codigo basados en una condicional
		
		String DaysOfWeek = "Jueves";
		
		switch(DaysOfWeek) {
		case "Lunes" : 
			System.out.println("Hoy es Lunes");
			break;
		case "Martes" : 
			System.out.println("Hoy es Martes");
			break;
		case "Miercoles" : 
			System.out.println("Hoy es Miercoles");
			break;
		case "Jueves" : 
			System.out.println("Hoy es Jueves");
			break;
		case "Viernes" : 
			System.out.println("Hoy es Viernes");
			break;
		
		}
	}
	
}
